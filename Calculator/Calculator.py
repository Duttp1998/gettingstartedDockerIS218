import math


class Calculator:

    def __init__(self):
        pass

    def addition(self, a, b):
        return a + b

    def subtraction(self, a, b):
        return a - b

    def multiplication(self, a, b):
        return a * b

    def division(self, a, b):
        return a / b

    def square(self, a, b):
        return a ** b

    def root(self, a):
        return math.sqrt(a)
